TOPIC_IDS: set[str] = {t["id"] for t in TOPICS}


def get_topic(topic_id: str) -> dict | None:
    """Return full topic dict by id, or None if topic unknown."""
    for t in TOPICS:
        if t["id"] == topic_id:
            return t
    return None


def get_topics_summary() -> list[dict]:
    """Return short list of topics for the picker screen — no step details."""
    return [
        {
            "id": t["id"],
            "title": t["title"],
            "emoji": t["emoji"],
            "short": t["short"],
            "duration": t["duration"],
            "steps_count": len(t["steps"]),
        }
        for t in TOPICS
    ]
